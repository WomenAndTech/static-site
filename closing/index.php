        <h2></h2>
        <p class="type-xsm italic">June 6, 2019</p>
        <p>We launched this little passion project back in 2012 with a team of six: founder <a href="https://twitter.com/cassiemc">Cassie McDaniel</a>, <a href="https://twitter.com/ivonnekn">Ivonne Karamoy</a>, <a href="https://twitter.com/britburger">Mark Staplehurst</a>, <a href="https://twitter.com/brttb">Brett Bergman</a>, <a href="https://twitter.com/milang">Milan Gokhale</a> and <a href="https://twitter.com/raykao">Ray Kao</a>. At the time there were few diversity and inclusion initiatives, and conversations around equal pay were not commonly discussed publicly. We wanted to do something about the lack of female voices in our industry so we set out to highlight the women working in tech, to get to know them, their story and their experiences working in the industry. We hoped that by doing so we would encourage other women and girls to celebrate their own stories, to highlight their intelligence, work and unique experiences.</p>
        <p>Women&&Tech was volunteer based from the start. While we sought sponsorship to help pay for the project costs, there was never any intention to monetize Women&&Tech. We wanted to maintain the passion, the integrity and the authenticity of the project. In 2017, we redesigned the website and became more intentional about representing not only women's stories but different experiences from
          a more diverse group of women. Over the years we’ve heard from so many amazing women doing incredible things in tech. Women just starting out in their careers as well as those who’ve reached the top level in our industry. We heard from women with different paths, experiences and perspectives. Today, these stories are much easier to find. Look to Medium or your favorite podcast to find them. The interview format has been done many times over and people are sharing their own stories. Conversations and initiatives around diversity, inclusion, and intersectionality have become more commonplace. We know there is still a long way to go but the time has come for our project to end. It’s time to make space for other projects, other voices and to discover what else we can do. We’re proud of the work we’ve done and have seen our mandate come to fruition: To amplify stories of women working in tech. </p>

        <p>The Women&&Tech project would not have been possible without our team and the community at large. We’d like to thank both the tech and the Toronto community for supporting us through the years. </p>

