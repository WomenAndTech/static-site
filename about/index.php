      <h2>Letter to our Readers</h2>
      <p>We’ve relaunched! Let us tell you why…</p>
      <p>This little project launched in 2012; a time when gender equality conversations were just gaining momentum. We founded Women && Tech to challenge the idea of tech as a male-dominated field. Our goal was to invite and interview 50 women we admire out for a coffee, talk to them about their work, their experiences and their awesomeness, and share these conversations with the world. By sharing these stories, we wanted to recognize the efforts of women in this industry and contribute to a community that elevates one another.</p>
      <p>We’ve interviewed designers, developers, entrepreneurs and multi-faceted women who are shaping and navigating the tech industry. Though we started in Toronto, our readers came from all over the world.  We've shared contributions from people who have interviewed women in their own cities and communities who are smart and are contributing to the tech industry in their own ways. We feel humbled and encouraged by the response and reach we’ve had in the community.</p>

      <h3 class="spacer30">The Publishing Process</h3>
      <p>Women && Tech has always been a passion project and behind the scenes we’ve shifted team members, changed hands and struggled to keep up with a publishing schedule that was burdened by an inefficient publishing process. </p>
      <p>While we loved our old website&mdash;and we know you did too&mdash;the publishing process behind it was inefficient and not scalable. Our initial idea for the site included crafting and designing each interview to reflect the personality of each woman. The idea never came to fruition because custom designing and developing each interview took too much time on top of conducting and transcribing the interview. We realized that though the design of the website was important, custom designing each interview was unnecessary. What was important was the content, the people and the stories. So we focused on that. And we’re glad we did.</p>
      <p>However, a fundamental issue remained. Our site architecture still required custom work and development to publish each interview. That’s where we lost our stride. Maintaining our publishing schedule with such a hefty production process became a bottleneck. Our plans over the years to overhaul the site was stalled by life. Work, family and other projects took priority and this little passion project of ours suffered from lack of consistency. </p>
      <p>Throughout all of this, one thing was clear: these individual stories were important. We know you’ve loved the interviews. Even with an inconsistent publishing schedule, we’ve maintained a large mailing list, and our Twitter community has grown to over 10,000 since we launched. We can’t thank you enough for supporting us and taking part in our conversation. So, although our team and the project has wavered, there’s still a community out there. We needed to ask ourselves some hard questions: Would we end the project? Did we still want to work on it? Had the passion died? Why is our contribution important?</p>
      <p>We knew something needed to change for the project to continue. We needed to regroup and figure out what this project would stand for.</p>

      <h3 class="spacer30">Diversity and Inclusion</h3>
      <p>We know there's a lot of really supportive communities out there for women in tech. Gender diversity in tech continues to be important the conversation has grown. It’s wonderful to see that people and organizations are trying to improve the landscape.</p>
      <p>However, we felt that our contributions were not enough. This project was meant to showcase gender diversity in tech. But it has become all too easy to equate diversity to mean more women and, though not necessarily intentional, mostly white women and entrepreneurs. Within our own nominations we’ve noticed this pattern. In addition to the lack of racial diversity, many other underrepresented groups and roles were not being elevated. We want to do better. Our mandate from the start was to speak to women from all walks of life. We need to be true to that mission if we want to improve the landscape. It's easy to make half-hearted diversity efforts and sometimes we fall short of the mark. But though it may get uncomfortable, it does start with looking at ourselves and how we can do better. We need to constantly question and critique our community, our leaders, our co-workers, our families, and ourselves. What are we doing to improve the landscape? What can we do in our own circles to make a change?</p>

      <h3 class="spacer30">So, what now?</h3>
      <p>First, we’re simplifying, refocusing and rebooting. We're simplifying our site architecture and our process, refocusing on the stories, and rebooting our project.</p>
      <p>We rebranded. We loved our old site design but it was time for a refresh, and an identity that reflects the diversity that is core to this project.</p>
      <p>Second, we’re making a bigger effort to be more inclusive. We want to share stories of women from different roles, backgrounds and walks of life. This means we’ll be looking for more diversity in our nominations. We hope you’ll continue to introduce us to these women. In sharing their stories, we feature the individual and help to paint a fuller picture of our community and our industry.</p>
      <p>We’re still not endorsing the obvious. We’re commending the awesome. </p>
      <p>Thank you for sticking by us and contributing to the conversation. We hope you continue to support us, to get to know the people that make our industry great, and to find comfort and inspiration in their stories.</p>
      <p>We've breathed new life into this project and welcome any support. <a href="https://www.patreon.com/WomenAndTech">Become a Patreon</a> and donate to our project if you can. This will help us continue to provide real, meaningful, and diverse stories. </p>

      <p>To contribute an interview or volunteer with the team, please <a href="mailto:volunteer@womenandtech.com">email us</a>.</p>

