      <dt class="interview-q">Did your family read a lot when you were growing up?</dt>
      <!-- // Answer -->
      <dd class="interview-a">
        <p>My parents actually wanted me to watch more TV and read less, but I found it pretty good to ignore what they told me to do. I had a great teacher in Grade 6, Mr. Smith. He had a creative writing and reading curriculum that was atypical of most other Grade 6 programs. We read and did creative writing exercises every day with him. Up until that point, it seemed predestined that I was going to be some kind of doctor or scientist, like every typical, Chinese immigrant's dream! The thought of having a creative career didn’t cross my mind until I realized that I was actually a pretty good writer, a creative writer.</p>
      </dd>
    </dl>

    <!-- // Q & A -->
    <dl class="border">
      <!-- // Question -->
      <dt class="interview-q">I saw in my research you contributed to an Android programming book. How did you come to combine your interests in writing with computing?</dt>
      <!-- // Answer -->
      <dd class="interview-a">
        <p>I left Regina to go to the University of Ottawa. I started out in marketing but
          I didn’t like it, so I switched to computer science. Although I loved those
          classes, I didn’t see myself becoming a computer scientist. So I applied to a
          Fine Arts program at <a href="http://www.ryerson.ca/index.html" target="_blank">Ryerson University</a> in Toronto called <a href="http://imagearts.ryerson.ca/newmedia.html" target="_blank">New Media</a> which was, at that time, part of a department called “Image Arts” and it had a mix of film and photography, along with new media. It focused on interactive art installations, which were fairly new at the time. That meant learning how to use electronics to sense when people were in rooms or to react to people. It was so
          different from anything else that I knew.</p>
          <p>My electronics teacher at that time, <a href="http://en.wikipedia.org/wiki/Norman_White">Norman White</a>
          (Ryerson got him to come out of retirement to come and teach), was awesome because he was so good at encouraging people, and he listened. He took my class to the engineering
          department where they actually had a 3D printer, before 3D printers were all the
          rage. It was an eye opener to see that this kind of technology existed.</p>
      </dd>
    </dl>

    <!-- // Q & A -->
    <dl class="border">
      <!-- // Question -->
      <dt class="interview-q">What did you do after you graduated?</dt>
      <!-- // Answer -->
      <dd class="interview-a">
        <p>I really liked the creative aspect of mixing technology and people, but it was difficult to find a job as an interactive art installation-ist. I had the opportunity to work with a startup that was doing large, Flash web applications. There were only eight people there when I started, so I was doing everything from HTML and CSS to writing content. I once sewed curtains for them [laughs]. If you had some sort of ability and could fill in the gaps, you did.</p>
        <p>That was really good for me as a learning experience because I didn’t get pushed into a narrow job description. At night I would still do physical art installations or other projects like that.</p>
      </dd>
    </dl>

    <!-- // Q & A -->
    <dl class="border">
      <!-- // Question -->
      <dt class="interview-q">You spent some time at the CFC (Canadian Film Centre) Media Lab. How did that come about?</dt>
      <!-- // Answer -->
      <dd class="interview-a">
        <p>After working at the startup I was looking for a change. And an
        opportunity came up to go to <a href="http://welcome.cfcmedialab.com">CFC Media Lab</a> on a full scholarship for a five
        month intensive post-graduate program. The program brought in experts to teach
        day or one week sessions with a couple key faculty members. Within two and a
        half months you were supposed to form a group and execute a prototype. </p>
        <p>After that I did a bit of freelance work, and then I returned to
          the CFC to work full-time as the research and technology manager. I was the
          point person for technical questions and resources. Students might come to me
          and say, “I want to make an interactive project with brain reading technology,
          where do I start?” Then I would help them research it and learn how to build it
          themselves. If they weren't technically savvy enough to want to do it on their
          own, or if time was short, then I would either work on their project or hire a
          contractor and manage them. </p>
      </dd>
