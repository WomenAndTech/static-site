      <!-- // Q & A -->
      <dl class="border">
        <!-- // Question -->
        <dt class="interview-q">Let’s talk about your background a little bit. What was growing up like?</dt>
        <!-- // Answer -->
        <dd class="interview-a">
          <p>I grew up in <a href="http://www.viennava.gov/">Vienna, Virginia</a>, a suburb of Washington DC. I’m the middle child in a Chinese-American family, so I had a very academic and conservative upbringing. One thing my family valued was that whatever we did, we were expected to do it excellently, whether that was academics, music, or sports. My parents taught me to put everything into what I do, and I’ve definitely carried that lesson with me, throughout my career and especially in founding Skilljar.</p>
        </dd>
      </dl>

      <!-- // Q & A -->
      <dl class="border">
        <!-- // Question -->
        <dt class="interview-q">You studied engineering at MIT then later went to Stanford for an MBA. Where did the interest in technology come from, and what led you down that academic path?</dt>
        <!-- // Answer -->
        <dd class="interview-a">
          <p>I’ve always been very analytical. In middle school I was on the math team. I went to a science and technology focused high school so it was always part of my academic upbringing. At <a href="http://engineering.mit.edu/">MIT</a> I got to be part of a larger community of people that were really excited by technology and innovation. I always thought I wanted to be an engineer, and by the time I got to college it just arose very naturally.</p>
        </dd>
      </dl>

