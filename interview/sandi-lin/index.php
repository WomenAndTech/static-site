       <!-- // Question -->
       <dt class="interview-q">What is the toughest thing you’ve had to do in your career?</dt>
       <!-- // Answer -->
       <dd class="interview-a">
         <p>Definitely the hardest things I’ve had to do are around letting employees go. Even if it is completely mutual it has never been a pleasant experience. I think a lot about what didn’t work in terms of the role, the environment, or the expectations. I reflect on what I could have done differently and where things went off track. A lot of times it’s not anyone’s “fault,” it’s just a mismatch of expectations, where people are in their lives, or what’s needed for the company. However, it’s still definitely tough.</p>
       </dd>
     </dl>

     <!-- // Q & A -->
     <dl class="border">
       <!-- // Question -->
       <dt class="interview-q">Who would you say is your leadership role model that has influenced your career path?</dt>
       <!-- // Answer -->
       <dd class="interview-a">
         <p>I really admire a leader at Amazon named Tom Taylor. When I worked on <a href="http://services.amazon.com/content/fulfillment-by-amazon.htm">Amazon Fulfillment</a> we were a really small team and I interacted with Tom on a daily basis; he’s actually one of our investors here at Skilljar. Tom is incredibly smart but also very humble and approachable. He provides strategic direction while also giving room for people to figure out the details. He has a remarkable ability to inspire others. I’m really lucky to have met him early in my career, and I definitely look to him as a role model for leadership.</p>
       </dd>
     </dl>

     <!-- // Q & A -->
     <dl class="border">
       <!-- // Question -->
       <dt class="interview-q">If you had to choose one characteristic what would you say is the most important trait to being a good leader?</dt>
       <!-- // Answer -->
       <dd class="interview-a">
         <p>This is tough. I would say listening. Even if you don’t agree with someone’s point of view, an effective leader has to relate to the people around them. If people feel like they are heard and their opinions are valued you’ll be much more effective.</p>
       </dd>
     </dl>

     <!-- // Q & A -->
     <dl class="border">
       <!-- // Question -->
       <dt class="interview-q">You’ve claimed to be more introverted than extroverted so how do you manage to network?</dt>
       <!-- // Answer -->
       <dd class="interview-a">
         <p>[Laughs] In general I’m not someone that enjoys “networking.” If I’ve had a long day and I have an event to attend, I mentally reframe it as an opportunity to tell people about what we’re doing at Skilljar and share our mission. Usually I consider a networking event a success if I walk away with one or two interesting contacts.</p>
       </dd>
     </dl>

