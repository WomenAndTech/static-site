     <!-- // Q & A -->
     <dl class="border">
       <!-- // Question -->
       <dt class="interview-q">Where do you see the education technology space heading over the next few years?</dt>
       <!-- // Answer -->
       <dd class="interview-a">
         <p>I think of education technology in two buckets. The first is technology that improves learning outcomes. These are things like adaptive learning and gamification. There’s a lot of exciting things happening in the K-12 space. The second bucket is technology that improves learning access. That’s a totally different way that technology can be applied to education. A lot of big data initiatives which help different education systems communicate are in this space, as well as Skilljar.</p>
       </dd>
     </dl>

     <!-- // Q & A -->
     <dl class="border">
       <!-- // Question -->
       <dt class="interview-q">Do you notice a lack of women starting companies in the tech industry? If you do, why do you think that might be the case?</dt>
       <!-- // Answer -->
       <dd class="interview-a">
         <p>Yes, absolutely. The numbers I’ve seen state around <a href="http://www.forbes.com/sites/fridapolli/2013/07/29/we-are-the-3-where-are-all-the-women-tech-entrepreneurs/">around 3% of tech companies are founded by women</a>. Of those companies their founders are typically non-technical and in my experience most of them have either been launching fashion-related e-commerce sites or fitness applications so if you take those out then there’s even fewer women leading technology companies.</p>
       </dd>
     </dl>

     <!-- // Q & A -->
     <dl class="border">
       <!-- // Question -->
       <dt class="interview-q">What do you think is causing this?</dt>
       <!-- // Answer -->
       <dd class="interview-a">
         <p>It’s a mix of reasons. First, women tend to be more risk-averse, and entrepreneurship is a very risky career move. Second, women are very underrepresented in the tech industry. When you combine women not necessarily wanting entrepreneurial risk with a lack of women in the technology industry you end up in a situation where only 3% of technology companies are started by women.</p>
       </dd>
     </dl>

