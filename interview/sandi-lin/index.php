      <!-- // Q & A -->
      <dl class="border">
        <!-- // Question -->
        <dt class="interview-q">You left your career at Amazon to start your own company. Was that a tough decision for you?</dt>
        <!-- // Answer -->
        <dd class="interview-a">
          <p>When I left Amazon I wasn’t scared about trying entrepreneurship – it’s something I had wanted to do for a while. After thinking through it, I realized the worst that could happen is the startup wouldn’t go anywhere, I would draw down my savings, and I would end up back at Amazon. This didn't seem so bad because I was pretty happy with my career progression there. I was actually more scared about leaving my team behind at Amazon. I was a manager at that point and had helped build the business <a href="http://local.amazon.com/">Amazon Local</a>. I felt a lot of loyalty towards the people I worked with so I was more worried about leaving them than I was about the future.</p>
        </dd>
      </dl>

      <!-- // Q & A -->
      <dl class="border">
        <!-- // Question -->
        <dt class="interview-q">Tell us about your company Skilljar.</dt>
        <!-- // Answer -->
        <dd class="interview-a">
          <p>Skilljar is an online course delivery platform which allows anyone to create an online course by uploading their videos and other content and publishing it to their website. We take care of registration, payments, course delivery, and analytics. We have instructors of all different sizes working across many different topics -- everything from yoga to enterprise software training. <a href="http://www.geekwire.com/2013/examazonians-raise-768k-online-instruction-platform-everpath/">We started the company in 2013</a>. It’s actually changed quite a bit from our original concept. We started as a search directory for online courses. As we began working with instructors, it became clear that what they really wanted was a technology platform where they could deliver online courses more easily. So we listened to our customers, and changed our product in September of 2013.</p>
        </dd>
      </dl>

      <aside class="border pull-quote">
