      <!-- // Q & A -->
      <dl class="border">
        <!-- // Question -->
        <dt class="interview-q">What has been the best and most difficult parts of running a company?</dt>
        <!-- // Answer -->
        <dd class="interview-a">
          <p>Creating something from scratch and adding value to the world through my team’s efforts is an incredible feeling. We have a lot of customers who really love using our product, and we’re powering a significant part of their business.</p>
          <p>The most difficult part is wearing so many different hats at the same time. I’ll respond to a complicated customer service issue, then pitch a really important customer, and then fix some code in our marketing website. This might all happen within the space of two hours, so I’m constantly switching roles and having to take on challenges I might never have done before.</p>
        </dd>
      </dl>

      <!-- // Q & A -->
      <dl class="border">
        <!-- // Question -->
        <dt class="interview-q">How do you create balance between work and your personal life?</dt>
        <!-- // Answer -->
        <dd class="interview-a">
          <p>Not very well [laughs]. Balance is an ongoing struggle for me. Exercising is very important so I try to do that as often as I can. I try hard to not work on Friday nights and Saturdays too, but that’s not always possible when you are trying to grow a company.</p>
        </dd>
      </dl>

      <!-- // Q & A -->
      <dl class="border">
        <!-- // Question -->
        <dt class="interview-q">What is your strongest asset in your technical career?</dt>
        <!-- // Answer -->
        <dd class="interview-a">
          <p>As a technical person I’ve always had a natural curiosity about how things work. I was the kid that played with Mouse Trap and those types of toys. From a technical perspective I’m fascinated by how bits and bytes move around, and because of that natural and genuine curiosity I think I’ve always been able to work very well with technical people.</p>
        </dd>
      </dl>

