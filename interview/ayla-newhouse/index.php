    <dl class="border">
      <dt class="interview-q">So to define technology you turned to technology?</dt>
      <dd class="interview-a">
        <p>Yes I did! So here’s a definition I wrote down from Merriam-Webster.</p>
        <p style="margin-left:2em;">
          Technology is
          <em>
            “a capability given by the practical application of knowledge”.
          </em>
        </p>
        <p>
          The example they give is
          <em>“a car’s fuel saving technology.”</em>
          Or, I wrote,
          <em>“a person’s emotional intelligence technology.”</em>
          What are those tools and process that help you have a higher emotional intelligence?
        </p>
      </dd>
    </dl>

