      <dt class="interview-q">What kind of tools do you use in your practice?</dt>
      <!-- // Answer -->
      <dd class="interview-a">
        <p>I use a computer with the Adobe Suite for design work, but I also use paper, pens, sticky notes, cameras and other recording devices.</p>
