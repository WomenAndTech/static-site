    <dl class="border">
      <!-- // Question -->
      <dt class="interview-q">What happened after you graduated?</dt>
      <!-- // Answer -->
      <dd class="interview-a">
        <p>I started a book publishing company, which obviously was a crazy idea when you look at the technology around self-publishing today. I was the art director and designer and had partners who took care of the business aspects.</p>
        <p>I was excited about the opportunity to be part of a cultural movement of some sort. We were publishing art-related books, plays, poetry, short story anthologies. It was small-scale, but local and inspiring. It gave people an opportunity to publish when they may not have otherwise. I liked that aspect of it.</p>
      </dd>
    </dl>

