    <!-- Q&A -->
    <dl class="border">
      <dt class="interview-q">Tell us more about your background.</dt>
      <dd class="interview-a">
        <p>My background spans arts, science and business. I was in real estate since I was a kid. My dad sent me to show the apartments. At university, I studied neuroscience and ran a clothing line that I had started in high school. I sold to stores in Toronto, and I had a job in a research lab. So I’ve always been able to move back and forth between art and science, and I find both completely stimulating. When I graduated, I continued to work at the research lab and opened my own clothing store on College Street, expanding to sell my line to small boutiques across North America.</p>
        <p>I was looking for a way to talk about the self and understand the self from scientific and artistic perspectives. I started working with brainwaves in <a href="http://n1nlf-1.eecg.toronto.edu/" title="Steve Mann's Personal Web Page" target="_blank">Steve Mann’s lab</a> almost a decade ago. Ultimately we created a system that I took out of the lab and started to commercialize with my two co-founders, Trevor Colemen and Chris Aimone. That was the formation of InteraXon, which has existed for the last five years. <a href="http://www.youtube.com/watch?v=uJS8EJwGx3o" target="_blank" title="InteraXon Bright Ideas Promotional Video on YouTube">Our first major project</a> was the Vancouver 2010 Winter Olympics. As our kick off project, it wasn’t a bad one to do! Since then, we’ve worked with companies like Deutsche Telekom, brands like Wrigley’s and agencies like the <a href="http://news.cnet.com/8301-17938_105-20009785-1.html#ixzz11PCoiU7v" title="Leslie Katz's report on InteraXon's thought-controlled in-flight entertainment systems published on cnet.com" target="_blank">Ontario Aerospace Council</a> to enable this technology across multiple domains.</p>
      </dd>
