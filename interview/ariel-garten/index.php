    <dl class="border">
      <dt class="interview-q">Why do you think there aren’t more young women choosing to pursue a career in science?</dt>
      <dd class="interview-a">
        <p>There’s definitely a tendency where people can, from a young age, identify, ‘I’m good at this, I’m not good at this, and this is scary.’ And if you decide you aren’t good at something, you don’t take the time to build the skill. You shut down and you don’t engage with it.</p>
        <p>I’m also a psychotherapist. I had a patient recently who said, “Numbers scare me. You say the word finance and I want to jump under the table over there.” And it was a process of moving aside her fear, seeing these things as neutral, allowing herself to open up to numbers so that she could spend time learning without feeling anxiety and insecurity.</p>
        <p>Insecurity is definitely an issue when you are growing up. And when you do something that you don’t know the answer to, your response to not knowing will determine your ability to learn it.</p>
        <p>If your response to not knowing is, ‘I don’t know it, therefore I feel uncomfortable, therefore I want to run away because it makes me feel insecure,’ then you’re not going to have the confidence, patience, willingness and openness to sit down and say, ‘I don’t know this but I'm not scared, I'm just going to sit here and figure it out.’ Your early responses and experiences will determine whether you go into a subject or not.</p>
        <p><em>[Editor's Note: Ariel took the stage at Toronto’s 2011 TEDx conference <a href="http://www.tedxtoronto.com/talks/redefining-consciousness-ariel-garten-ceo-and-co-founder-interaxon/" title="Ariel's TED talk on Redefining Consciousness" target="_blank">to give a speech</a> on how thought-controlled computing may work to help us understand the self. It's Ariel's fascination for knowing the self which led her to submerge herself in art, study neuroscience and later become a psychotherapist.]</em></p>
      </dd>
