      <h3>Guidelines for Nominating or Interviewing Someone</h3>
      <p>The main criteria is that the interviewee inspires you. But there are a few other things we like to consider.</p>
      <ul>
        <li>Diversity - Age, background, level of experience & more</li>
        <li>Is the interviewee’s story different somehow from those already featured on the site?</li>
        <li>We want to tell stories of people who are usually not highlighted in the media</li>
