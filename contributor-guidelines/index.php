      <h3>Process for Conducting an Interview</h3>
      <p>Email <a href="mailto:volunteer@womenandtech.com">volunteer@womenandtech.com</a>. It’s as simple as that.</p>
      <p>Please include in your email:<p>
        <ul>
          <li>An introduction to yourself and how you heard about us</li>
          <li>Who you’d like to interview and why. Please include links to their work, website, etc</li>
          <li>Your writing or interviewing experience, including links or samples if available</li>
          <li>Whether or not you have access to a camera or photographer</li>
        </ul>
      <p>We encourage you to meet up for a coffee with your heroes at any time, but if you’d like to be published under Women && Tech, we kinda need to know your plans so we can build that into our publishing queue. This also allows us to speak to you beforehand and help you determine a direction for the interview that will work for both of us.</p>
      <p>&nbsp;</p>

      <h3>What is the time commitment?</h3>
      <p>Each interview involves upfront research, the interview itself, transcriptions, editing, visuals/photography and development. It takes about 25 hours from start to finish. Your contribution should account for about half of that time. We'll help you with editing, the visuals and development. We do expect an edited transcription. Often a final interview is a total of 5-7000 words (we’ve had a couple that were 10-12,000 words!), but we aim to cut these down to around 2-3000 words for the final draft. We would like your help taking at least a first pass to whittle it down to the core of your conversation.</p>
      <p>&nbsp;</p>

